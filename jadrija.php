						<p>
							Jadrija je mjesto s vikendicama i privatnim kućama za odmor, nalazi se između Šibenika i Vodica , te je okružena borovom šumom, brojnim uvalama, te bistrim morem. Idealno je odredište za obitelji s djecom, te za sve one koji traže privatnost, a ujedno je i vrlo blizu Vodica i Šibenika za one koji traže provod.
						</p>
						<p>
							Zbog razvedene obale šibenskog arhipelaga i blizine nacionalnih parkova Krka i Kornati, Jadrija je odlična polazna točka za izlete u okolicu. Preporučili bi posjet staroj jezgri Šibenik, Vodicama, otocima Zlarin, Prvić, Žirije, Kaprije, te spomenutim nacionalnim parkovima Krka i Kornati. Također, tu je i velik broj manjih otočića i uvala za sve one željne privatnosti i uživanja u suncu i bistrini Jadranskog mora. Jadrija je postala omiljeno mjesto u Šibeniku i regiji za sportske entuzijaste i one željne adrenalina.
						</p>
						<p>
							Zbog povoljnih vjetrova, Jadrija je poznato okupljalište surfera, te je domaćin regate prvenstva hrvatske u jedrenju na dasci. Kako za iskusne, tako je i za početnike organizirana škola jedrenja i iznajmljivanje windsurf opreme. Osim jedrenja, na raspolaganju su i brojni drugi sportovi kao odbojka na pijesku, nogomet, košarka, tenis, stolni tenis, mini golf, boćanje.Tokom ljetnih mjeseci svakodnevno je povezana sa Šibenikom pomorskom prugom, koja vozi kroz jedan od najljepših tjesnaca na obali – kanalom Sv. Ante.
